#!/usr/bin/env node
// Generates client/src/services/category/configs/generated/configManifest.ts
// Scans config files under client/src/services/category/configs/** (excluding generated/**)
// and produces a typed manifest mapping keys (e.g., fashion-women) to dynamic import functions.

import fs from 'fs';
import path from 'path';

const repoRoot = path.resolve(process.cwd());
const configsRoot = path.resolve(repoRoot, 'client', 'src', 'services', 'category', 'configs');
const outDir = path.resolve(configsRoot, 'generated');
const outFile = path.resolve(outDir, 'configManifest.ts');

/** Recursively walk directory and collect .ts files (exclude .d.ts) */
function walk(dir, acc = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    if (entry.name.startsWith('.')) continue;
    const full = path.join(dir, entry.name);
    const relFromConfigs = path.relative(configsRoot, full);
    if (relFromConfigs.startsWith('generated')) continue; // skip generated
    if (entry.isDirectory()) {
      walk(full, acc);
    } else if (entry.isFile()) {
      if (entry.name.endsWith('.ts') && !entry.name.endsWith('.d.ts')) {
        acc.push(full);
      }
    }
  }
  return acc;
}

function toPosix(p) {
  return p.split(path.sep).join('/');
}

/** Build config key from relative path under configs root */
function buildKey(relPath) {
  // Example: fashion/women.ts -> fashion-women
  const noExt = relPath.replace(/\.ts$/, '');
  // Split and join with '-'
  const parts = noExt.split(/[/\\]/).filter(Boolean);
  return parts.join('-');
}

function main() {
  if (!fs.existsSync(configsRoot)) {
    console.error('Configs root not found:', configsRoot);
    process.exit(1);
  }
  const files = walk(configsRoot);
  // Build manifest entries
  const entries = [];
  for (const abs of files) {
    const rel = path.relative(configsRoot, abs);
    // Only include files inside a category subdirectory (exclude root-level TS like ConfigurationRegistry.ts)
    if (!rel.includes(path.sep)) continue;
    // Skip index.ts files that don't represent standalone keys
    if (/\bindex\.ts$/.test(rel)) continue;
    const key = buildKey(rel);
    // Use extensionless import paths for Vite/TS resolution
    const withoutExt = rel.replace(/\.ts$/, '');
    const importPath = toPosix(path.join('..', withoutExt));
    entries.push({ key, importPath });
  }

  // Sort keys for stable output
  entries.sort((a, b) => a.key.localeCompare(b.key));

  const header = `/** Auto-generated by scripts/generate-config-manifest.mjs. Do not edit by hand. */`;
  const bodyLines = [];
  bodyLines.push(`export const CONFIG_MANIFEST = {`);
  for (const { key, importPath } of entries) {
    bodyLines.push(`  "${key}": () => import("${importPath}"),`);
  }
  bodyLines.push(`} as const;`);
  bodyLines.push(`export type ConfigKey = keyof typeof CONFIG_MANIFEST;`);

  const outContent = [header, '', ...bodyLines, ''].join('\n');
  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(outFile, outContent, 'utf8');
  console.log(`Config manifest generated: ${path.relative(repoRoot, outFile)} with ${entries.length} entries.`);
}

main();


